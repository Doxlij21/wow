function showRoles(){

        $(".availableRoles").css('display','block');
        $(".showRoles").css('display','none');
        $(".hideRoles").css('display','block');

}
function showPermissions() {

        $(".availablePermissions").css('display','block');
        $(".showPermissions").css('display','none');
        $(".hidePermissions").css('display','block');


}
function hideRoles() {
    $(".availableRoles").css('display','none');
    $(".showRoles").css('display','block');
    $(".hideRoles").css('display','none');
}
function hidePermissions() {
    $(".availablePermissions").css('display','none');
    $(".showPermissions").css('display','block');
    $(".hidePermissions").css('display','none');
}